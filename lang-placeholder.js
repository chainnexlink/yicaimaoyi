// Hook into language switching to translate placeholders
(function() {
    const origUpdatePageContent = window.updatePageContent || (typeof updatePageContent !== 'undefined' ? updatePageContent : null);

    function translatePlaceholders(lang) {
        if (lang !== 'zh' && placeholderTranslations[lang]) {
            const phDict = placeholderTranslations[lang];
            document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(function(el) {
                const ph = el.getAttribute('placeholder');
                if (ph && phDict[ph]) {
                    el.setAttribute('data-ph-original', ph);
                    el.setAttribute('placeholder', phDict[ph]);
                }
            });
        } else if (lang === 'zh') {
            document.querySelectorAll('[data-ph-original]').forEach(function(el) {
                el.setAttribute('placeholder', el.getAttribute('data-ph-original'));
                el.removeAttribute('data-ph-original');
            });
        }
    }

    // Override updatePageContent to also translate placeholders
    if (typeof updatePageContent === 'function') {
        const _origUpdate = updatePageContent;
        window.updatePageContent = function(lang) {
            _origUpdate(lang);
            translatePlaceholders(lang);
        };
        // Re-assign for non-window reference
        updatePageContent = window.updatePageContent;
    } else {
        // If updatePageContent not yet defined, run on DOMContentLoaded
        document.addEventListener('DOMContentLoaded', function() {
            if (typeof updatePageContent === 'function') {
                const _origUpdate = updatePageContent;
                window.updatePageContent = function(lang) {
                    _origUpdate(lang);
                    translatePlaceholders(lang);
                };
                updatePageContent = window.updatePageContent;
            }
        });
    }
})();